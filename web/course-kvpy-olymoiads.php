			<h1>Olympiad Exams</h1>
			<p>&nbsp;</p>

			<p>The Mathematical Olympiad Programme in India, which leads to participation of Indian students in the International Mathematical Olympiad (IMO) is organized by the Homi Bhabha Centre for Science Education (HBCSE) on behalf of the National Board for Higher Mathematics (NBHM) of the Department of Atomic Energy (DAE), Government of India. This programme is one of the major initiatives undertaken by the NBHM. Its main purpose is to spot mathematical talent among pre-university students in the country.<br />
			For the purpose of training and selection of students for the Olympiad contest, 25 regions all over the country have been designated and each assigned a Regional Coordinator (RC). Additionally, three groups (Central Board of Secondary Education (CBSE), Navodaya Vidyalaya Samiti (NVS) and Kendriya Vidyalaya Sangathana (KVS) have a &lsquo;Regional Coordinator&rsquo; each. The Mathematical Olympiad programme consists of six stages.</p>

			<p>&nbsp;</p>
			<p>The Mathematical Olympiad program consists of six stages. The Six Stages are</p>

			<ul>
				<li><strong>Stage 1:</strong>&nbsp;Pre Regional Mathematical Olympiad (PRMO)</li>
				<li><strong>Stage 2:</strong>&nbsp;Regional Mathematical Olympiad (RMO)</li>
				<li><strong>Stage 3:</strong>&nbsp;Indian National Mathematical Olympiad (INMO)</li>
				<li><strong>Stage 4:&nbsp;</strong>International Mathematical Olympiad Training Camp (IMOTC)</li>
				<li><strong>Stage 5:</strong>&nbsp;Pre-departure Training Camp for IMO</li>
				<li><strong>Stage 6:</strong>&nbsp;International Mathematical Olympiad (IMO)</li>
			</ul>

			<p>&nbsp;</p>

			<p><strong>Stage 1: Pre Regional Mathematical Olympiad (PRMO)</strong></p>

			<ul>
				<li>PMRO is the first stage for IMO organized by IAPT.</li>
				<li>Test paper will contain 30 Questions.</li>
				<li>List of top 300 students who will be eligible for RMO from each region will be declared by mid September</li>
				<li>Details of Examination fees and other details are available on the IAPT Website:&nbsp;<a href="http://iapt.org.in/">http://iapt.org.in/</a></li>
				<li>Eligibility: Not older than 19 years of age as on 1st August and, in addition, studying in Class XI and below</li>
			</ul>

			<p>&nbsp;</p>

			<p><strong>Stage 2: Regional Mathematical Olympiad (RMO)</strong></p>

			<ul>
				<li>strong>&nbsp;</strong>Three Hours Written Test with six problems</li>
				<li>Up to 30 students selected from each of the 25 regions for Stage 3 &ndash; INMO.</li>
				<li>Conducted by Regional Coordinators under the guidance of HBCSE.</li>
				<li>For EGMO participation up to five girls would be additionally selected apart from the regional quota of 30 students &ndash; independent of the number of girls who may be part of the general quota of 30 students.</li>
				<li>Results of all the RMOs are declared centrally by the HBCSE.</li>
				<li>Eligibility: Students who qualify PRMO</li>
			</ul>

			<p>&nbsp;</p>

			<p><strong>Stage 3: Indian National Mathematical Olympiad (INMO)</strong></p>

			<ul>
				<li>Usually held on the third Sunday of January</li>
				<li>Four Hours Written Test</li>
				<li>Top 30 Students from the country are chosen in order of merit</li>
				<li>Next 45 Students who are in Class XI or lower are given INMO Certificate of Merit and are eligible to appear in INMO next year without going through the RMO, provided they are not in Class XII at the time of writing INMO.</li>
				<li>In addition to the top 30, up to five girl students will be chosen based on their performance in INMO for participation in EGMO</li>
				<li>Eligibility: Students who are selected in RMO in the previous year and those holding a INMO Certificate of Merit</li>
			</ul>

			<p>&nbsp;</p>

			<p><strong>Stage 4: International Mathematical Olympiad Training Camp (IMOTC)</strong></p>

			<ul>
				<li>Month long training camp in April-May at the Homi Bhabha Centre for Science Education (HBCSE), Mumbai for INMO Awardees and the girls selected for EGMO.</li>
				<li>INMO Awardees of previous years eligible for IMO and EGMO are also invited, provided they have satisfactorily gone through postal tuition throughout the year.</li>
				<li>On the basis of a number of selection tests through the Camp, a team of the best six students is selected from the combined pool of junior and senior batch participants.</li>
				<li>The junior students will receive INMO certificate and a prize in the form of books. The senior students will receive a prize in the form of books and cash.</li>
			</ul>

			<p>&nbsp;</p>

			<p><strong>Stage 5: Pre-departure Training Camp for IMO</strong></p>

			<ul>
				<li>Final round of training and orientation for the selected six students for ten days prior to departure for IMO and EGMO.</li>
			</ul>

			<p>&nbsp;</p>

			<p><strong>Stage 6: International Mathematical Olympiad (IMO)</strong></p>

			<ul>
				<li>Two written tests held on two consecutive days</li>
				<li>Each test consists of three problems</li>
				<li>Duration: Four and Half Hours</li>
				<li>Selection of team members for IMO will be subject to their fulfilling criteria such as age limit, medical fitness, parental consent and others as specified by the council</li>
				<li>Each selected student needs to have a valid Indian passport meeting the visa regulations of the host country.</li>
			</ul>

			<p>&nbsp;</p>

			<p><strong>Syllabus:</strong></p>

			<p>Pre-degree college mathematics - arithmetic of integers, geometry, quadratic equations and expressions, trigonometry, co-ordinate geometry, system of linear equations, permutations and combination, factorization of polynomial, inequalities, elementary combinatory, probability theory and number theory, finite series and complex numbers and elementary graph theory The syllabus does not include calculus and statistics. The major areas from which problems are given are algebra, combinatory, geometry and number theory. The syllabus is in a sense spread over Class XI to Class XII levels, but the problems under each topic involve high level of difficulty and sophistication. The difficulty level increases from RMO to INMO to IMO. Previous years Question papers available on the HBCSE website can provide a good idea of the types of question one can expect.</p>
			
			<p>&nbsp;</p>

			<p><strong>INMO Scholarship:</strong></p>

			<p>The INMO awardees who successfully complete IMOTC and who pursue B.Sc., or other courses as per the guidelines of the Board with mathematics as one of the principal subjects of study, are eligible to receive a scholarship of National Board of Higher Mathematics, which is at present Rs. 4,000 per month through the period of their undergraduate studies (subject to satisfactory progress). Enhance scholarship would be available for pursuing masters studies in Mathematics.</p>
			
