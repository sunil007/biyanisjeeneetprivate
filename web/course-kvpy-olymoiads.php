			<p><strong>Enrollment Process:</strong></p>

			<p>1. This stage is conducted by The Indian Association of Physics Teachers (IAPT)</p>

			<p>2. Registration usually starts in the month of September.</p>

			<p>3. Check with Principal or Head of Department/Institution whether your school/college is a registered centre. If yes, all formalities can be completed in house.</p>

			<p>4. If one&rsquo;s school/college isn&rsquo;t a Registered NSE Centre, a student has to approach a nearby Registered NSE Centre for enrollment. The IAPT (Indian Association of Physics Teachers) website will provide all the relevant information pertaining to locating a Registered NSE Centre and the enrollment process.</p>

			<p>Details of Enrollment Process, Syllabus, Exam Details, Examination Fees and other details are available on the IAPT website:&nbsp;<a href="http://iapt.org.in/" target="_blank">http://iapt.org.in/</a></p>

			<p>&nbsp;</p>
			<p><strong>Stage II: Indian National Olympiad (INO)</strong></p>

			<p>This Stage II is organized by The Homi Bhabha Centre for Science Education (HBCSE). Organized in the Subjects of Astronomy, Biology, Chemistry, Physics (INAO, INBO, INChO, INPO). In order to appear in the Stage II exam a student has to satisfy some clauses pertaining to Stage I, for this follow website.</p>

			<p>&nbsp;</p>
			<p><strong>Stage III: Orientation cum Selection Camps (OCSC)</strong></p>

			<p>OCSC is conducted at HBCSE in Mumbai. Selection based on performance in INOs for a particular subject. The number of selections in a particular subject to be announced before the INO examinations.</p>

			<p>&nbsp;</p>
			<p><strong>Stage IV: Training of Indian Teams for International Olympiads at HBCSE</strong></p>

			<p>Selected teams undergo a rigorous training program at HBCSE in theory and experiments usually for one/two weeks or longer depending on the subject where special laboratories are set up at HBCSE for Astronomy and Observational Astronomy. Experts and Resource Persons from different fields are invited to the training camps.</p>

			<p>&nbsp;</p>
			<p><strong>Stage V: Participation in International Olympiad (IO)</strong></p>

			<p>The selected students are for each subject is teamed together with 2-3 Teacher Leaders to accompany them for the International Olympiads which are held in a new participating country every year either in the month of July or December each year.</p>

